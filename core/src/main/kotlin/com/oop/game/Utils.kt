package com.oop.game;

object Utils {
	fun progressBar(value: Int): String {
		return when {
			value == 100	-> "[####################]";
			value > 90		-> "[###################-]";
			value > 85		-> "[##################--]";
			value > 80		-> "[#################---]";
			value > 75		-> "[################----]";
			value > 70		-> "[###############-----]";
			value > 65		-> "[##############------]";
			value > 60		-> "[#############-------]";
			value > 55		-> "[############--------]";
			value > 50		-> "[###########---------]";
			value > 45		-> "[##########----------]";
			value > 40		-> "[#########-----------]";
			value > 35		-> "[########------------]";
			value > 30		-> "[#######-------------]";
			value > 25		-> "[######--------------]";
			value > 20		-> "[#####---------------]";
			value > 15		-> "[####----------------]";
			value > 10		-> "[###-----------------]";
			value > 5		-> "[##------------------]";
			value > 0		-> "[#-------------------]";
			else			-> "[--------------------]";
		};
	}
}
