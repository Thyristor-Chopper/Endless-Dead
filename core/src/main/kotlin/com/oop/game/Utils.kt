package com.oop.game;

object Utils {
	/**
	 * 미터기를 그린다.
	 *
	 * @param value	실수 진행률(0.0~1.0)
	 */
	fun progressBar(value: Float): String {
		return progressBar((value * 100.0f).toInt());
	}
	
	/**
	 * 미터기를 그린다.
	 *
	 * @param value	정수 진행률(1~100)
	 */
	fun progressBar(value: Int): String {
		return when {
			value == 100	-> "[####################]";
			value > 90		-> "[###################-]";
			value > 85		-> "[##################--]";
			value > 80		-> "[#################---]";
			value > 75		-> "[################----]";
			value > 70		-> "[###############-----]";
			value > 65		-> "[##############------]";
			value > 60		-> "[#############-------]";
			value > 55		-> "[############--------]";
			value > 50		-> "[###########---------]";
			value > 45		-> "[##########----------]";
			value > 40		-> "[#########-----------]";
			value > 35		-> "[########------------]";
			value > 30		-> "[#######-------------]";
			value > 25		-> "[######--------------]";
			value > 20		-> "[#####---------------]";
			value > 15		-> "[####----------------]";
			value > 10		-> "[###-----------------]";
			value > 5		-> "[##------------------]";
			value > 0		-> "[#-------------------]";
			else			-> "[--------------------]";
		};
	}
}
